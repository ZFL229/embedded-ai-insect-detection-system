/* 控制 RESET 引脚完成一次 OV2640 硬件复位。 */
void OV2640_HardwareReset(void)
{
    HAL_GPIO_WritePin(DCMI_RESET_GPIO_Port, DCMI_RESET_Pin, GPIO_PIN_RESET);
    HAL_Delay(20);

    HAL_GPIO_WritePin(DCMI_RESET_GPIO_Port, DCMI_RESET_Pin, GPIO_PIN_SET);
    HAL_Delay(50);
}

/* 读取厂家 ID 和产品 ID，用于确认 SCCB 通信和摄像头在线。 */
uint8_t OV2640_ReadID(uint8_t *mid_h, uint8_t *mid_l, uint8_t *pid, uint8_t *ver)
{
    uint8_t ok = 1;

    ok &= BSP_SCCB_ReadReg(OV2640_SCCB_ADDR, 0x1C, mid_h);
    ok &= BSP_SCCB_ReadReg(OV2640_SCCB_ADDR, 0x1D, mid_l);
    ok &= BSP_SCCB_ReadReg(OV2640_SCCB_ADDR, 0x0A, pid);
    ok &= BSP_SCCB_ReadReg(OV2640_SCCB_ADDR, 0x0B, ver);

    return ok;
}

/* 写入单个 OV2640 寄存器。 */
uint8_t OV2640_WriteReg(uint8_t reg, uint8_t data)
{
    return BSP_SCCB_WriteReg(OV2640_SCCB_ADDR, reg, data);
}

/* 顺序写入寄存器表，直到遇到 0xFF/0xFF 结束标记。 */
static uint8_t OV2640_WriteRegList(const uint8_t (*list)[2])
{
    uint8_t ok = 1;
    uint16_t i = 0;

    while (!(list[i][0] == 0xFF && list[i][1] == 0xFF))
    {
        ok &= OV2640_WriteReg(list[i][0], list[i][1]);
        HAL_Delay(1);
        i++;
    }

    return ok;
}

/* 初始化 320x240 JPEG 输出。 */
uint8_t OV2640_Init_320x240_JPEG(void)
{
    uint8_t ok = 1;

    ok &= OV2640_WriteRegList(OV2640_JPEG_INIT);
    ok &= OV2640_WriteRegList(OV2640_YUV422);
    ok &= OV2640_WriteRegList(OV2640_JPEG);

    HAL_Delay(10);

    ok &= OV2640_WriteReg(0xff, 0x01);
    HAL_Delay(10);
    ok &= OV2640_WriteReg(0x15, 0x00);

    ok &= OV2640_WriteRegList(OV2640_320x240_JPEG);

    return ok;
}

