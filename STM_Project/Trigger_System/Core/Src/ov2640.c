/* 设置光照/白平衡模式，mode=0 为自动。 */
uint8_t OV2640_SetLightMode(uint8_t mode)
{
    switch (mode)
    {
    case 0:
        return OV2640_WriteRegList(OV2640_LIGHT_MODE_AUTO);
    case 1:
        return OV2640_WriteRegList(OV2640_LIGHT_MODE_SUNNY);
    case 2:
        return OV2640_WriteRegList(OV2640_LIGHT_MODE_CLOUDY);
    case 3:
        return OV2640_WriteRegList(OV2640_LIGHT_MODE_OFFICE);
    case 4:
        return OV2640_WriteRegList(OV2640_LIGHT_MODE_HOME);
    default:
        return 0;
    }
}
